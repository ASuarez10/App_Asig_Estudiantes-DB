CREATE TABLE HEADQUARTERS_CAREERS(
	ID_HEADQUARTER_CAREER NUMBER DEFAULT ID_INCREMENT_HC.NEXTVAL,
	ID_HEADQUARTER NUMBER,
	ID_CAREER NUMBER,
	CONSTRAINT PK_HEADQUARTERS_CAREERS PRIMARY KEY (ID_HEADQUARTER_CAREER),
