CREATE TABLE CANDIDATES(
	ID_CANDIDATE VARCHAR2(11),
	NAME VARCHAR2(50),
	LASTNAME VARCHAR2(50),
	ID_TYPE VARCHAR2(10),
	SEX VARCHAR2(15),
	CITY VARCHAR2(50),
	ESTATE VARCHAR2(50),
	ICFES_GENERAL NUMBER,
	AGE INTEGER,
	ID_HEADQUARTER_CAREER NUMBER,
	ID_EDUCATION NUMBER,
